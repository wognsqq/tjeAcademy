package codes;

public class MainDrive {

	public static void main(String[] args) {
		
		int num1 = 30;
		int num2 = 30; 
		
		System.out.println(num1 > num2); //false
		System.out.println(num1 >= num2);//true
		System.out.println(num1 < num2);//false
		System.out.println(num1 <= num2);//true
		
		System.out.println(num1 == num2);//true
		System.out.println(num1 != num2);//false
	}
}
